#include<iostream>
using namespace std;

int main(){
    int a;
    cout<<"Size of Integer: "<<sizeof(a)<<"\n";
    float b;
    cout<<"Size of Float: "<<sizeof(b)<<"\n";
    double c;
    cout<<"Size of Double: "<<sizeof(c)<<"\n";
    char d;
    cout<<"Size of Character: "<<sizeof(d)<<"\n";
    bool e;
    cout<<"Size of Boolean: "<<sizeof(e)<<"\n";
    short int si;
    cout<<"Size of Short int: "<<sizeof(si)<<"\n";
    long int li;
    cout<<"Size of Long int: "<<sizeof(li)<<"\n";
    return 0;
}